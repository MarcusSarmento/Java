//Aula 3 - Varuiáveis e Tipos Primitivos em Java
public class start {
   
    public static void main(String[] args) {
        int idade = 25;
        System.out.println("Minha idade é : " + idade);

        double altura = 1.75;
        System.out.println("Minha altura é : " + altura);

        char sexo = 'M';
        System.out.println("Meu sexo é : " + sexo);

        boolean estaVivo = false;
        estaVivo = true;
        System.out.println("Estou vivo? : " + estaVivo);

        final String nome = "Marcus Vinícius Del' Duca de Souza Sarmento";
        System.out.println("Meu nome é : " + nome);
    }
}
